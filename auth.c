/*
 * 文件：auth.c
 * 说明：认证与授权功能实现文件
 * 
 * 本文件实现了用户登录、注册和权限验证功能，是整个系统安全机制的核心部分。
 * 
 * 主要功能包括：
 * - 检查用户是否有指定角色权限（支持管理员拥有所有权限的特殊规则）
 * - 登出当前用户，清空会话信息
 * 
 * 权限模型说明：
 * - 患者 (ROLE_PATIENT)：只能访问患者相关功能
 * - 医生 (ROLE_DOCTOR)：只能访问医生相关功能
 * - 管理员 (ROLE_MANAGER)：拥有所有权限，可以访问任何功能
 * 
 * 本文件实现认证与授权功能的原因：
 * 如果没有权限控制，任何人都能修改医生的诊断记录或者查看其他患者的隐私信息，
 * 这将导致严重的安全问题。所以这个文件负责确保每个人只能做自己该做的事。
 */

#include "models.h"
#include <stdio.h>
#include <string.h>
#include <stdlib.h>

/* 全局登录会话 */
/* 
 * g_session 是一个全局变量，用于保存当前登录用户的会话信息。
 * 
 * 这个变量在整个程序运行期间都有效，任何函数都可以通过它来确认：
 * - 你是谁（username）
 * - 你有什么权限（role）
 * - 你是否已经登录（isLoggedIn）
 * 
 * 包含以下字段：
 * - isLoggedIn: 登录状态标志，1 表示已登录，0 表示未登录
 * - role: 用户角色（患者/医生/管理员），决定用户可以访问哪些功能
 * - userId: 关联的用户 ID（患者的病历号或医生的工号），管理员时为 0
 * - username: 登录用户名，用于显示和权限验证
 * 
 * 使用全局变量的原因：
 * 因为程序中几乎所有地方都需要知道"当前是谁在操作"，
 * 如果每次都通过参数传递会很麻烦，所以用一个全局变量来记录。
 */
extern UserSession g_session;

/*
 * 函数：check_permission - 检查用户是否有指定角色权限
 * 
 * 功能说明：
 *   判断当前登录的用户是否拥有执行某项操作所需的角色权限。
 *   这是系统安全机制的核心函数，在进行敏感操作前都应调用此函数进行权限检查。
 * 
 * 参数：
 *   requiredRole - 执行该操作需要的最低角色类型
 * 
 * 返回值：
 *   1 - 有权限，可以执行操作
 *   0 - 无权限，禁止执行操作
 * 
 * 权限判断逻辑：
 *   1. 首先检查用户是否已登录，未登录直接返回无权限
 *   2. 管理员拥有"超级权限"，可以访问所有功能，直接返回有权限
 *   3. 其他角色必须精确匹配所需角色才有权限
 * 
 * 使用示例：
 *   if (!check_permission(ROLE_DOCTOR)) {
 *       printf("您没有权限执行此操作。\n");
 *       return;
 *   }
 * 
 * 此函数的重要性：
 *   如果没有这个检查，任何人都可以假装成医生开处方、修改病历，
 *   系统将无法保证数据安全。所以每次进行重要操作前，都要先进行权限验证。
 */
int check_permission(UserRole requiredRole)
{
    /* 第一步：检查用户是否已登录，未登录肯定没有权限 */
    if (!g_session.isLoggedIn)
    {
        return 0;  /* 返回 0 表示"无权访问" */
    }
    
    /* 第二步：管理员拥有所有权限，这是系统的特殊规则 */
    /* 管理员角色 (ROLE_MANAGER) 可以访问任何需要特定角色的功能 */
    if (g_session.role == ROLE_MANAGER)
    {
        return 1;  /* 返回 1 表示"有权限" */
    }
    
    /* 第三步：普通用户必须角色完全匹配才有权限 */
    /* 角色不匹配时返回 0 表示无权限，例如医生角色访问患者专属功能会被拒绝 */
    return g_session.role == requiredRole;
}

/*
 * 函数：logout_user - 登出当前用户
 * 
 * 功能说明：
 *   清除当前用户的登录会话信息，将系统状态恢复到未登录状态。
 *   通常在用户主动退出登录或会话超时时调用此函数。
 * 
 * 清除的内容包括：
 *   - 登录状态标志设为未登录 (0)
 *   - 角色重置为默认的患者角色
 *   - 用户 ID 清零
 *   - 用户名为空字符串
 * 
 * 注意：此函数只清除内存中的会话信息，不影响磁盘上的账号数据。
 * 下次仍然可以使用相同的账号密码重新登录。
 * 
 * 清除会话信息的原因：
 * 如果不清除，其他人用同一台电脑时就会自动以上一个用户的身份登录，
 * 可以冒充别人看病历、开处方。所以离开时一定要注销。
 */
void logout_user(void)
{
    /* 清除登录状态，标记为用户未登录 */
    g_session.isLoggedIn = 0;
    /* 重置角色为默认值（患者） */
    g_session.role = ROLE_PATIENT;
    /* 清除关联的用户 ID */
    g_session.userId = 0;
    /* 清空用户名字符串，第一个字符设为'\0'表示空字符串 */
    g_session.username[0] = '\0';
}
